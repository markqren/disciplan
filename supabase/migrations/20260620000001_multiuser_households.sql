-- FEA: Multi-user households (phase 1 schema foundation)
--
-- Adds a `households` grouping + a `profiles` map (auth user -> owner) and
-- stamps every user-data table with `owner` (text) + `household_id` (bigint).
-- Existing rows are backfilled to owner='mark' in the seeded household, so the
-- app behaves identically until single-person views are used.
--
-- Security note: RLS policies remain permissive (USING(true)) for now. The
-- columns added here make a future per-household / per-owner RLS tightening a
-- policy-only change (see plan Phase 6). No RLS is enforced yet.
--
